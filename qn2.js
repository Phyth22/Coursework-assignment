function squareNumbers() {
    for (let i = 1; i <= 10; i++) {
      // Calculate the squared value using the exponentiation operator for efficiency
      const squaredValue = i ** 2;
  
      // Display the result in clear and consistent formatting
      console.log(`The square of ${i} is ${squaredValue}.`);
    }
  }
  
  // Call the function to execute and display the results
  squareNumbers();
  


